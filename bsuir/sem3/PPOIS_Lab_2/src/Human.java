public class Human {
    private String age;
    private String name;
    public Human(String age, String name){
        this.age = age;
        this.name = name;
    }

    /**
     *
     * @return возраст
     */
    public String getAge() {
        return age;
    }

    /**
     *
     * @return имя
     */
    public String getName() {
        return name;
    }

}
